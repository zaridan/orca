import { formatStats } from './non-terminal-benchmark-stats'
import type { GitResult } from './git-worktree-refresh-benchmark'
import type { SidebarResult } from './sidebar-row-projection-benchmark'

export function renderReport(sidebarResults: SidebarResult[], gitResults: GitResult[]): string {
  const sidebarRows = sidebarResults
    .map(
      (result) =>
        `| ${result.scenario} | ${result.worktrees} | ${result.rows} | ${formatStats(result.stats)} |`
    )
    .join('\n')
  const gitRows = gitResults
    .map(
      (result) =>
        `| ${result.scenario} | ${result.repos} | ${result.worktreesPerRepo} | ${result.returnedWorktrees} | ${result.gitProcessesPerIteration} | ${formatStats(result.wallStats)} |`
    )
    .join('\n')

  return `# Non-terminal performance investigation: sidebar rows and git refresh

Generated by \`pnpm exec vitest run --config config/vitest.non-terminal-perf.config.ts\`.

## Scope

This investigation covers items 2 and 3 from \`NON_TERMINAL_PERFORMANCE_BENCHMARK_IDEAS.md\` and intentionally avoids Terminal/PTY implementation files. These are local opt-in benchmark samples, not CI pass/fail budgets.

## Item 2: sidebar/worktree-list row projection

Measured the pure row-building path used by \`WorktreeList\`: \`buildRows(...)\`, plus host-section projection where relevant. This does not claim full React commit time, but it isolates the deterministic CPU work that feeds the virtualized sidebar rows.

| Scenario | Worktrees | Output rows | Timing |
| --- | ---: | ---: | ---: |
${sidebarRows}

### Item 2 findings

- The row projection does not look like a sustained bottleneck at 1,000 worktrees: medians were single-digit milliseconds in this sample, and the heavier grouping paths are the ones to watch in a full React/Electron profile.
- Active worktree switches should not rebuild \`rows\` because the main \`useMemo(buildRows)\` dependency list does not include \`activeWorktreeId\`; the active-card visual update is a separate path.
- The heavier measured path is PR grouping; host sections add another partitioning step but were not the slowest path in this sample.
- If users report sidebar lag at very large workspace counts, the next measurement should be a real React Profiler/Electron run for commit time and visible-row render counts. The pure projection is not obviously the bottleneck at 1,000 rows.

## Item 3: git/worktree refresh subprocess fanout

Measured actual \`listWorktrees(repoPath)\` calls against temporary git repositories with real linked worktrees. Git subprocess count is captured with a temporary PATH shim around \`git\`, while wall time is measured around the \`listWorktrees\` call itself.

| Scenario | Repos | Extra worktrees per repo | Returned worktrees | Git processes / iteration | Timing |
| --- | ---: | ---: | ---: | ---: | ---: |
${gitRows}

### Item 3 findings

- Current \`listWorktrees\` uses one \`git worktree list --porcelain -z\` subprocess per repo refresh on current git.
- Sparse-checkout annotation no longer adds N git subprocesses per worktree; it uses filesystem reads/stat through \`resolveGitDir\` + \`info/sparse-checkout\` checks.
- The remaining churn risk is repo fanout: \`fetchAllWorktrees\` and IPC event handlers can still refresh every repo concurrently, so total git process count scales with repo count rather than worktree count.
- If users report idle CPU from git refresh, benchmark the event source next: count how often \`worktrees:changed\`, startup hydration, and explicit refresh paths call all repos versus one touched repo.

## Suggested next actions

1. Add an Electron/React Profiler pass for item 2 only if we can reproduce sidebar interaction lag; the pure row projection does not currently look expensive enough to justify speculative optimization.
2. For item 3, instrument refresh callers to count repo-wide fanout over a 60-second idle/event window. The suspected issue is repeated refresh frequency, not per-worktree subprocess explosion.
3. Keep any future fixes outside Terminal/PTY files until the terminal model PRs settle.
`
}
